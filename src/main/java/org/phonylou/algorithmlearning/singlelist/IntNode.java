package org.phonylou.algorithmlearning.singlelist;

public class IntNode {
    private int val;
    private IntNode next;

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public IntNode getNext() {
        return next;
    }

    public void setNext(IntNode next) {
        this.next = next;
    }
}
